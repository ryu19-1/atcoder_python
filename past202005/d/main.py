#!/usr/bin/env python3

def main():
    N = int(input())
    s = [input() for _ in range(5)]
    tmp = [['###.','#.#.','#.#.','#.#.','###.'],
           ['.#..','##..','.#..','.#..','###.'],
           ['###.','..#.','###.','#...','###.'],
           ['###.','..#.','###.','..#.','###.'],
           ['#.#.','#.#.','###.','..#.','..#.'],
           ['###.','#...','###.','..#.','###.'],
           ['###.','#...','###.','#.#.','###.'],
           ['###.','..#.','..#.','..#.','..#.'],
           ['###.','#.#.','###.','#.#.','###.'],
           ['###.','#.#.','###.','..#.','###.']]


    #4列で1つの数字を表す
    ans = ''
    for i in range(N):
        t = [v[1+4*i:5+4*i] for v in s]
        for i in range(10):
            if t == tmp[i]:
                ans += str(i)

    print(ans)



if __name__ == "__main__":
    main()
